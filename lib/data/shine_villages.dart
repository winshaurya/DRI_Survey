// SHINE Villages data structure based on PRA Survey Revenue Village for App.xlsx

class ShineVillage {
  final String shineCode;
  final String panchayat;
  final String panchayatLgdCode;
  final String revenueVillage;
  final String rvLgdCode;
  final String block;
  final String district;
  final String state;
  final String praTeam;
  final int? emailCount;

  const ShineVillage({
    required this.shineCode,
    required this.panchayat,
    required this.panchayatLgdCode,
    required this.revenueVillage,
    required this.rvLgdCode,
    required this.block,
    required this.district,
    required this.state,
    required this.praTeam,
    this.emailCount,
  });

  factory ShineVillage.fromExcelRow(List<dynamic> row) {
    return ShineVillage(
      shineCode: row[1]?.toString() ?? '',
      panchayat: row[2]?.toString() ?? '',
      panchayatLgdCode: row[3]?.toString() ?? '',
      revenueVillage: row[5]?.toString() ?? '',
      rvLgdCode: row[6]?.toString() ?? '',
      block: row[7]?.toString() ?? '',
      district: row[8]?.toString() ?? '',
      state: row[9]?.toString() ?? '',
      praTeam: row[10]?.toString() ?? '',
      emailCount: int.tryParse(row[11]?.toString() ?? ''),
    );
  }

  Map<String, dynamic> toMap() {
    return {
      'shine_code': shineCode,
      'panchayat': panchayat,
      'panchayat_lgd_code': panchayatLgdCode,
      'revenue_village': revenueVillage,
      'rv_lgd_code': rvLgdCode,
      'block': block,
      'district': district,
      'state': state,
      'pra_team': praTeam,
      'email_count': emailCount,
    };
  }
}

