  // Helper methods to get unique values for autocomplete
  static List<String> getUniqueShineCodes() {
    return villages.map((v) => v.shineCode).toSet().toList()..sort();
  }

  static List<String> getUniqueVillages() {
    return villages.map((v) => v.revenueVillage).where((v) => v.isNotEmpty).toSet().toList()..sort();
  }

  static List<String> getUniquePanchayats() {
    return villages.map((v) => v.panchayat).where((v) => v.isNotEmpty).toSet().toList()..sort();
  }

  static List<String> getUniqueBlocks() {
    return villages.map((v) => v.block).where((v) => v.isNotEmpty).toSet().toList()..sort();
  }

  static List<String> getUniqueDistricts() {
    return villages.map((v) => v.district).where((v) => v.isNotEmpty).toSet().toList()..sort();
  }

  static List<String> getUniqueStates() {
    return villages.map((v) => v.state).where((v) => v.isNotEmpty).toSet().toList()..sort();
  }

  // Find village by SHINE code
  static ShineVillage? findByShineCode(String shineCode) {
    try {
      return villages.firstWhere((v) => v.shineCode.toLowerCase() == shineCode.toLowerCase());
    } catch (e) {
      return null;
    }
  }

  // Find villages by partial SHINE code match
  static List<ShineVillage> searchByShineCode(String query) {
    if (query.isEmpty) return [];
    return villages.where((v) =>
      v.shineCode.toLowerCase().contains(query.toLowerCase())
    ).toList();
  }

  // Find villages by village name
  static List<ShineVillage> searchByVillage(String query) {
    if (query.isEmpty) return [];
    return villages.where((v) =>
      v.revenueVillage.toLowerCase().contains(query.toLowerCase())
    ).toList();
  }

  // Find villages by panchayat
  static List<ShineVillage> searchByPanchayat(String query) {
    if (query.isEmpty) return [];
    return villages.where((v) =>
      v.panchayat.toLowerCase().contains(query.toLowerCase())
    ).toList();
  }
